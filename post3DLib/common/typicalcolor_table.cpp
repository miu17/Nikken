QColor TypicalColorTable::nameToColor(const QString &str)
{
    if (str == "(none)")
        return Qt::transparent;

    for ( int i = 0 ; i < tpColTable_size ; i++ ) {
        if ( tpColTable[i].name == str ) return QColor(tpColTable[i].rgb);
    }
    return Qt::transparent;
}

QString TypicalColorTable::colorToName(const QColor &color)
{
    // whiteとnoneが区別できない
    if (color == QColorConstants::Svg::white)
        return "white";

    for ( int i = 0 ; i < tpColTable_size ; i++ ) {
        if ( tpColTable[i].rgb == color.rgb() ) return tpColTable[i].name;
    }
    return QString();
}



